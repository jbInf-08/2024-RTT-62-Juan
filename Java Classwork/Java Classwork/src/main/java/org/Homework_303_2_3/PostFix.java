package org.Homework_303_2_3;

public class PostFix {
    public static void main(String[] args) {
        // Declare and initialize an integer variable
        int number = 5;

        // Print the initial value
        System.out.println("Initial value of number: " + number);

        // Use postfix increment operator to increase the value
        number++;

        // Print the value after incrementing
        System.out.println("Value of number after incrementing: " + number);
    }
}
